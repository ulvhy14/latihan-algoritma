#Tipe Data String
u = "Belajar Python di Duniailkom"
print(u)
 
#Tipe Data Integer
l = 1500
print(l)
 
#Tipe Data Float
f = 99.123
print(f)
 
#Tipe Data Complex Number
i = 4j
print(i)
 
#Tipe Data Boolean
n = True
print(n)
 
#Tipe Data List
a = ["satu","dua","tiga","satu"]
print(a)
 
#Tipe Data Tuple
b = ("satu","dua","tiga","satu")
print(b)
 
#Tipe Data Set
m = {"satu","dua","tiga","empat"}
print(m)
 
#Tipe Data Dictionary
y = {"satu":1, "dua":2.13, "tiga":"a", "empat": True}
print(y)